//Dias da semana

#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int dia;

int main(){

scanf("%d",&dia);

if (dia ==1){printf("Domingo");}
else if (dia ==2){printf("Segunda");}
else if (dia ==3){printf("Terça");}
else if (dia ==4){printf("Quarta");}
else if (dia ==5){printf("Quinta");}
else if (dia ==6){printf("Sexta");}
else if (dia ==7){printf("Sabado");}
else{printf("valor invalido");}

return 0 ;
}









